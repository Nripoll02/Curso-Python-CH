#Sumar todos los nuemeros del 1 al 100

# Inicializar la variable de suma
suma = 0

# Iterar sobre todos los números del 1 al 100
for numero in range(1, 101):
    # Sumar cada número a la variable de suma
    suma += numero

# Imprimir el resultado
print("La suma de todos los números hasta el 100 es:", suma)
